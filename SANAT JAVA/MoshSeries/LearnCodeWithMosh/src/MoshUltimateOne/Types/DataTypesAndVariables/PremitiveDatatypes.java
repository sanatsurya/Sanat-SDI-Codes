public class PremitiveDatatypes {
    public static void main(String[] args) {
        int a = 1;
        System.out.println(a);
        float b = 15243592345f;
        System.out.println(b);
        byte c = 8;
        short d = 78;
        double e =7.87;
        char f = 'h';
        boolean isTrue = true;
        System.out.print(c+"// " +d+" //"+ e +" //");
        System.out.println(f);
        System.out.println(isTrue);
    }
}
