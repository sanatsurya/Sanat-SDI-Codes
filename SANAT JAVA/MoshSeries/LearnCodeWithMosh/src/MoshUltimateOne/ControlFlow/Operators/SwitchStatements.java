package MoshUltimateOne.ControlFlow.Operators;

public class SwitchStatements {
    public static void main(String[] args) {
        int num = 17;
        switch (num){
            case 1:
                System.out.println();
                break;
            case 17:
                System.out.println("you have 17");
            default:
                System.out.println("numberis not found");
        }
    }
}
